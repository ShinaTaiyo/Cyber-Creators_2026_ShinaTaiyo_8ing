using namespace useful;

//============================================================================
// コンストラクタ
//============================================================================
COBB::COBB() :
	m_VtxMax(VEC3_INIT),
	m_VtxMin(VEC3_INIT),
	m_Rot(VEC3_INIT)
{
	SetType(CCollisionInfo::TYPE::OBB); //判定タイプを「OBB」に設定
}

//============================================================================
// デストラクタ
//============================================================================
COBB::~COBB()
{

}

//============================================================================
// 最大頂点設定
//============================================================================
void COBB::SetVtxMax(D3DXVECTOR3 VtxMax)
{
	m_VtxMax = VtxMax;
}

//============================================================================
// 最大頂点取得
//============================================================================
const D3DXVECTOR3& COBB::GetVtxMax() const
{
	return m_VtxMax;
}

//============================================================================
// 最小頂点設定
//============================================================================
void COBB::SetVtxMin(D3DXVECTOR3 VtxMin)
{
	m_VtxMin = VtxMin;
}

//============================================================================
// 最小頂点取得
//============================================================================
const D3DXVECTOR3& COBB::GetVtxMin() const
{
	return m_VtxMin;
}

//============================================================================
// 向き設定
//============================================================================
void COBB::SetRot(D3DXVECTOR3 Rot)
{
	m_Rot = Rot;
}

//============================================================================
// 向き取得
//============================================================================
const D3DXVECTOR3& COBB::GetRot() const
{
	return m_Rot;
}
